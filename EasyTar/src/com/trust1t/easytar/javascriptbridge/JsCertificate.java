public class JsCertificate {

	private String certificate;

	
	public JsCertificate(String certificate) {
		super();
		this.certificate = certificate;
	}

	public String getCertificate() {
		return certificate;
	}

	public void setCertificate(String certificate) {
		this.certificate = certificate;
	}
	
	
	
}
